function buildHomeActivities(
  concerns: string[],
  age: AgeGroup
): ScreeningReport["homeActivitiesSection"] {
  const activities: ActivityItem[] = [];

  for (const concern of concerns) {
    const byAge = CONCERN_ACTIVITIES[concern];
    if (byAge && byAge[age]) {
      activities.push(byAge[age]);
    }
  }

  const seen = new Set<string>();
  const unique: ActivityItem[] = [];
  for (const a of activities) {
    if (!seen.has(a.title)) {
      seen.add(a.title);
      unique.push(a);
    }
  }

  const maxActivities = 8;
  if (unique.length < 4) {
    const general = GENERAL_ACTIVITIES[age] ?? [];
    for (const a of general) {
      if (!seen.has(a.title) && unique.length < maxActivities) {
        seen.add(a.title);
        unique.push(a);
      }
    }
  }

  const trimmed = unique.slice(0, maxActivities);

  return {
    title: "Что можно начать делать дома",
    intro:
      trimmed.length > 0
        ? "Вот что вы можете начать делать уже сегодня. Выберите 2–3 упражнения и занимайтесь регулярно — лучше понемногу, чем долго и редко."
        : "Регулярные совместные занятия — лучший способ поддержки развития ребёнка.",
    activities: trimmed,
  };
}

